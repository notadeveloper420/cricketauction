export function fmtMoney(amount, leagueKey) {
  const l = LEAGUES[leagueKey];
  if (!l) return String(amount);
  if (leagueKey === "ipl") {
    if (amount >= l.crore) return `₹${(amount / l.crore).toFixed(2)}Cr`;
    return `₹${(amount / l.lakh).toFixed(0)}L`;
  }
  if (amount >= 1000000)
    return `${l.currency}${(amount / 1000000).toFixed(2)}M`;
  if (amount >= 1000) return `${l.currency}${(amount / 1000).toFixed(0)}K`;
  return `${l.currency}${amount}`;
}

