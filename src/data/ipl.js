export function iplIncrement(currentBid) {
  const cr = 10000000,
    l = 100000;
  if (currentBid < 1 * cr) return 5 * l; // ₹30L–₹95L   → +₹5L
  if (currentBid < 2 * cr) return 10 * l; // ₹1Cr–₹1.9Cr → +₹10L (0.1Cr)
  if (currentBid < 5 * cr) return 20 * l; // ₹2Cr–₹4.8Cr → +₹20L (0.2Cr)
  return 25 * l; // ₹5Cr+        → +₹25L (0.25Cr)
}
export function iplIncrementLabel(currentBid) {
  const inc = iplIncrement(currentBid);
  const cr = 10000000,
    l = 100000;
  if (inc >= cr) return `₹${inc / cr}Cr`;
  return `₹${inc / l}L`;
}

