export function buildIPLAuctionSets(retentions = {}, extraPlayers = []) {
  const retainedNames = new Set(
    Object.values(retentions)
      .flat()
      .map((p) => p.name),
  );
  const extraNames = new Set(extraPlayers.map((p) => p.name));
  const basePlayers = IPL_PLAYERS.filter(
    (p) => !retainedNames.has(p.name) && !extraNames.has(p.name),
  );
  const available = [
    ...basePlayers,
    ...extraPlayers.filter((p) => !retainedNames.has(p.name)),
  ];
  return IPL_SETS.filter((s) => s.id !== "ACC")
    .map((set) => ({
      ...set,
      players: available
        .filter((p) => p.iplSet === set.id)
        .sort(() => Math.random() - 0.5),
    }))
    .filter((s) => s.players.length > 0);
}

export function buildAcceleratedRound(unsoldPlayers) {
  return {
    ...IPL_SETS.find((s) => s.id === "ACC"),
    players: unsoldPlayers
      .filter((p) => p.capped)
      .sort(() => Math.random() - 0.5),
  };
}

export function classifyReleasedIPLPlayer(player) {
  const marquee = [
    "Virat Kohli",
    "Rohit Sharma",
    "Jasprit Bumrah",
    "Rishabh Pant",
    "Pat Cummins",
    "Mitchell Starc",
    "Shreyas Iyer",
    "Rashid Khan",
    "Heinrich Klaasen",
    "Nicholas Pooran",
    "Travis Head",
    "Jos Buttler",
    "Hardik Pandya",
    "Ravindra Jadeja",
    "Trent Boult",
    "Jofra Archer",
    "MS Dhoni",
  ];
  const base = marquee.includes(player.name)
    ? 20000000
    : player.os
      ? 7500000
      : 5000000;
  let iplSet = "BA1";
  if (player.role === "WK-Batter") iplSet = player.capped ? "WK1" : "UWK1";
  else if (player.role === "All-rounder")
    iplSet = player.capped ? "AL1" : "UAL1";
  else if (player.role === "Bowler") iplSet = player.capped ? "FA1" : "UFA1";
  return { ...player, base, iplSet };
}
